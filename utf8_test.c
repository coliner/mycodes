int utf8_len(const uint8_t *s) {
    int len = 0;
    while (1) {
        uint8_t c = *s++;
        if (c == '\0') break;
        if (c < 0x80) {
            ++len;
        } else if ((c & 0xf8) == 0xf0) {
            ++len;
            if (*s++ == '\0') break;
            if (*s++ == '\0') break;
            if (*s++ == '\0') break; 
        } else if ((c & 0xf0) == 0xe0) {
            ++len;
            if (*s++ == '\0') break;
            if (*s++ == '\0') break;
        } else if ((c & 0xe0) == 0xc0) {
            ++len;
            if (*s++ == '\0') break;
        } else {
            break;
        }
    }
    return len;
}

const char* my_decode(const uint8_t *s, int *code) {
    int ret;
    uint8_t c = *s++;
    if (c == '\0') return NULL;
    if (c < 0x80) {
        ret = c;
    } else if ((c & 0xf8) == 0xf0) {
        ret = (c & 0x07) << 18;
        if ((c = *s++) == '\0') return NULL;
        ret |= (c & 0x3f) << 12;
        if ((c = *s++) == '\0') return NULL;
        ret |= (c & 0x3f) << 6;
        if ((c = *s++) == '\0') return NULL;
        ret |= (c & 0x3f);
    } else if ((c & 0xf0) == 0xe0) {
        ret = (c & 0x0f) << 12;
        if ((c = *s++) == '\0') return NULL;
        ret |= (c & 0x3f) << 6;
        if ((c = *s++) == '\0') return NULL;
        ret |= (c & 0x3f);
    } else if ((c & 0xe0) == 0xc0) {
        ret = (c & 0x1f) << 6;
        if ((c = *s++) == '\0') return NULL;
        ret |= (c & 0x3f);
    } else {
        return NULL;
    }
    *code = ret;
    return s;
}

